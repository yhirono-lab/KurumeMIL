# slide(症例)を訓練用とテスト(valid)用に分割
def slide_split(train, test):
    # ex) train = '123', test_or_valid = '4'
    train_list = [i for i in train]
    data_map = {}
    data_map['data1'] = [DLBCL_1, FL_1, RL_1]
    data_map['data2'] = [DLBCL_2, FL_2, RL_2]
    data_map['data3'] = [DLBCL_3, FL_3, RL_3]
    data_map['data4'] = [DLBCL_4, FL_4, RL_4]
    data_map['data5'] = [DLBCL_5, FL_5, RL_5]

    train_DLBCL = []
    train_FL = []
    train_RL = []
    for num in train_list:
        train_DLBCL = train_DLBCL + data_map[f'data{num}'][0]
        train_FL = train_FL + data_map[f'data{num}'][1]
        train_RL = train_RL + data_map[f'data{num}'][2]

    test_DLBCL = data_map[f'data{test}'][0]
    test_FL = data_map[f'data{test}'][1]
    test_RL = data_map[f'data{test}'][2]

    return train_DLBCL, train_FL, train_RL, test_DLBCL, test_FL, test_RL
